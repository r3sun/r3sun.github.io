_2048_1680 = (1680  * 100 ) /1920
_2048_1440 = (1440  * 100 ) /1920
_2048_1280 = (1280  * 100 ) /1920
_2048_1024 = (1024  * 100 ) /1920
_2048_768 = (768  * 100 ) /1920

def to_1680(x):
    return ((x * _2048_1680) / 100)

def to_1440(x):
    return ((x * _2048_1440) / 100)

def to_1280(x):
    return ((x * _2048_1280) / 100)

def to_1024(x):
    return ((x * _2048_1024) / 100)

def to_768(x):
    return ((x * _2048_768) / 100)


def from_2048(x):
    print(f"x:\t{x}")
    print(f"1680px:\t{to_1680(x)}")
    print(f"1440px:\t{to_1440(x)}")
    print(f"1280px:\t{to_1280(x)}")
    print(f"1024px:\t{to_1024(x)}")

